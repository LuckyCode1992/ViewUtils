package com.justcode.hxl.viewutil.recycleview_util.layoutmanager.greedolayout;


public class Size {
    int mWidth;
    int mHeight;

    public Size(int width, int height) {
        mWidth = width;
        mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
